    <h2 class="text-primary">O que é o FGTS?</h2>
    <p>O FGTS (Fundo de Garantia por Tempo de Serviço) é um direito do trabalhador brasileiro com carteira assinada. Ele consiste em uma conta vinculada ao contrato de trabalho, onde o empregador deposita mensalmente 8% do salário do empregado. Esses valores podem ser utilizados em situações como compra de imóvel, aposentadoria, doenças graves, entre outras.</p>
    
    <h3 class="text-secondary">Antecipação do FGTS</h3>
    <p>A antecipação do FGTS é uma modalidade de crédito oferecida por instituições financeiras, permitindo que o trabalhador receba antecipadamente valores de saque-aniversário do FGTS. Nesse tipo de operação, o saldo futuro é utilizado como garantia, e os valores antecipados são descontados diretamente do saldo do FGTS na data de liberação do saque-aniversário.</p>
    <p>Essa modalidade é vantajosa para quem precisa de recursos imediatos, pois costuma oferecer taxas de juros mais baixas em comparação a outros tipos de crédito pessoal.</p>
    <p><strong>Como funciona:</strong></p>
    <ul>
        <li>O trabalhador precisa aderir à modalidade de saque-aniversário do FGTS.</li>
        <li>O banco faz uma análise de crédito e calcula o valor disponível para antecipação.</li>
        <li>O pagamento do empréstimo é feito diretamente pelo saldo do FGTS na data prevista.</li>
